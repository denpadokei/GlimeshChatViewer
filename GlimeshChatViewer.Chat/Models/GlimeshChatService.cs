        #region // パブリックメソッド
        public async Task Start()
        {
            this.connection = await GlimeshConnection.ConnectViaLocalhostOAuthBrowser(clientId, secretKey, scopes);
            if (connection == null) {
                return;
            }
            this.userModel = await this.connection.Users.GetCurrentUser();
            if (this.userModel == null) {
                return;
            }
            this.channelModel = await this.connection.Channel.GetChannelByName(this.userModel.username);
            if (channelModel == null) {
                return;
            }
            this.chatEventClient = await ChatEventClient.CreateWithToken(this.connection);
            this.chatEventClient.OnChatMessageReceived += this.ChatEventClient_OnChatMessageReceived;
            this.chatEventClient.OnFollowOccurred += this.ChatEventClient_OnFollowOccurred;

            if (!await this.chatEventClient.Connect()) {
                return;
            }
            if (!await this.chatEventClient.JoinChannelChat(this.channelModel.id) || !await this.chatEventClient.JoinChannelEvents(this.channelModel.id, this.userModel.id)) {
                return;
            }
        }
