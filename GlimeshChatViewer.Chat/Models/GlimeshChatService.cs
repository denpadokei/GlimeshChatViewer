        public GlimeshChatService()
        {
#if DEBUG
            var path = Path.Combine(Directory.GetCurrentDirectory(), "IDs.json");
            if (File.Exists(path)) {
                var jsonText = File.ReadAllText(path);
                var json = JObject.Parse(jsonText);
                if (json.TryGetValue("client_id", out var clientId)) {
                    this.clientId = clientId.Value<string>();
                }
                if (json.TryGetValue("secret_key", out var secret)) {
                    this.secretKey = secret.Value<string>();
                }
            }
#endif
        }

