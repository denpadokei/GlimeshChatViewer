using GlimeshChatViewer.Services.Interfaces;
using GlimeshChatViewer.Services.Interfaces.Models;
using Prism.Mvvm;
using StatefulModel;
using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Voiceroid2Sharp.Standard;
using Voiceroid2Sharp.Standard.Interfaces;

namespace GlimeshChatViewer.Services
{
    public class Voiceroid2Service : BindableBase, IVoiceroid2Service
    {
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プロパティ
        public ObservableSynchronizedCollection<VoiceroidEntity> Voiceroids { get; }
        /// <summary>説明 を取得、設定</summary>
        private bool isConnected_;
        /// <summary>説明 を取得、設定</summary>
        public bool IsConnected
        {
            get => this.isConnected_;

            private set => this.SetProperty(ref this.isConnected_, value);
        }

        /// <summary>説明 を取得、設定</summary>
        private VoiceroidEntity currentVoiceroid_;
        /// <summary>説明 を取得、設定</summary>
        public VoiceroidEntity CurrentVoiceroid
        {
            get => this.currentVoiceroid_;

            set => this.SetProperty(ref this.currentVoiceroid_, value);
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド用メソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // オーバーライドメソッド
        protected override void OnPropertyChanged(PropertyChangedEventArgs args)
        {
            base.OnPropertyChanged(args);
            if (args.PropertyName == nameof(this.CurrentVoiceroid)) {
                this.voiceroid2.CurrentVoiceroid = this.CurrentVoiceroid.VoiceroidName;
            }
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // パブリックメソッド
        public async Task Initializze()
        {
            if (this.voiceroid2.IsConnected) {
                return;
            }
            await this.voiceroid2.ConnectAsync(true);
            foreach (var voiceroid in this.voiceroid2.ActiveVoiceroids) {
                this.Voiceroids.Add(new VoiceroidEntity(voiceroid.VoiceroidName, voiceroid.Command));
            }
            this.IsConnected = true;
        }
        public async Task Reconnect()
        {
            if (this.voiceroid2.IsConnected) {
                return;
            }
            await this.voiceroid2.ConnectAsync(false);
            this.IsConnected = true;
        }
        public Task Talk(string message)
        {
            return this.voiceroid2.TalkAsync(message);
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プライベートメソッド
        private void Voiceroid2_OnDisconnected(object sender, EventArgs e)
        {
            this.IsConnected = false;
        }
        private void Voiceroids_PropertyChanged(object sender, PropertyChangedEventArgs e)
        {
            if (sender is VoiceroidEntity entity) {
                var voiceroid = this.voiceroid2.ActiveVoiceroids.FirstOrDefault(x => x.VoiceroidName == entity.VoiceroidName);
                if (voiceroid == null) {
                    return;
                }
                voiceroid.Command = entity.Command;
            }
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // メンバ変数
        private IVoiceroid2 voiceroid2;
        private bool disposedValue;
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // 構築・破棄
        public Voiceroid2Service()
        {
            this.voiceroid2 = new Voiceroid2();
            this.voiceroid2.OnDisconnected += this.Voiceroid2_OnDisconnected;
            this.Voiceroids = new ObservableSynchronizedCollection<VoiceroidEntity>();
            this.Voiceroids.PropertyChanged += this.Voiceroids_PropertyChanged;
        }
        protected virtual void Dispose(bool disposing)
        {
            if (!disposedValue) {
                if (disposing) {
                    // TODO: マネージド状態を破棄します (マネージド オブジェクト)
                    this.voiceroid2.OnDisconnected -= this.Voiceroid2_OnDisconnected;
                    this.voiceroid2.Dispose();
                }

                // TODO: アンマネージド リソース (アンマネージド オブジェクト) を解放し、ファイナライザーをオーバーライドします
                // TODO: 大きなフィールドを null に設定します
                disposedValue = true;
            }
        }

        // // TODO: 'Dispose(bool disposing)' にアンマネージド リソースを解放するコードが含まれる場合にのみ、ファイナライザーをオーバーライドします
        // ~Voiceroid2Service()
        // {
        //     // このコードを変更しないでください。クリーンアップ コードを 'Dispose(bool disposing)' メソッドに記述します
        //     Dispose(disposing: false);
        // }

        public void Dispose()
        {
            // このコードを変更しないでください。クリーンアップ コードを 'Dispose(bool disposing)' メソッドに記述します
            Dispose(disposing: true);
            GC.SuppressFinalize(this);
        }
        #endregion
    }
}
